/**
 * Tabla de Pact Magic (PHB p.107). Devuelve null para level 0.
 */
export function warlockPactMagic(level: number): PactMagic | null {
  if (level < 1 || level > 20) return null;
  // [count, slotLevel] por warlock level.
  const table: Array<[number, number]> = [
    [1, 1],  // 1
    [2, 1],  // 2
    [2, 2],  // 3
    [2, 2],  // 4
    [2, 3],  // 5
    [2, 3],  // 6
    [2, 4],  // 7
    [2, 4],  // 8
    [2, 5],  // 9
    [2, 5],  // 10
    [3, 5],  // 11
    [3, 5],  // 12
    [3, 5],  // 13
    [3, 5],  // 14
    [3, 5],  // 15
    [3, 5],  // 16
    [4, 5],  // 17
    [4, 5],  // 18
    [4, 5],  // 19
    [4, 5],  // 20
  ];
  const [count, slotLevel] = table[level - 1]!;
  return { slotCount: count, slotLevel };
}
